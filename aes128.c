void aes128_add_round_key (void)
{
    for (uint8_t row = 0u; row < 4u; row++)
    {
        for (uint8_t col = 0u; col < 4u; col++)
        {
            aes128_state[row][col] ^= aes128_round_key[row][col];
        }
    }
}

void aes128_substitute_bytes (void)
{
    for (uint8_t row = 0u; row < 4u; row++)
    {
        for (uint8_t col = 0u; col < 4u; col++)
        {
            aes128_state[row][col] = aes128_sBox[(((aes128_state[row][col]) & (0xF0u)) >> 4u)][((aes128_state[row][col]) & (0x0Fu))];
        }
    }
}

void aes128_shift_rows (void)
{
    for (uint8_t row = 1u; row < 4u; row++)
    {
        uint8_t shift = row;
        while (shift > 0u)
        {
            uint8_t col = 0u, temp = aes128_state[row][col];
            while (col < 3u)
            {
                aes128_state[row][col] = aes128_state[row][col + 1u];
                col++;
            }
            aes128_state[row][col] = temp;
            shift--;
        }
    }
}

void aes128_mix_columns (void)
{
    for (uint8_t state_col = 0u; state_col < 4u; state_col++)
    {
        uint8_t state_column[4u];
        for (uint8_t matrix_row = 0u; matrix_row < 4u; matrix_row++)
        {
            uint8_t col_sum = 0u;
            for (uint8_t matrix_col = 0u; matrix_col < 4u; matrix_col++)
            {
                uint8_t mul_res = aes128_state[matrix_col][state_col];
                if (aes128_column_mix_matrix[matrix_row][matrix_col] > 1u)
                {
                    mul_res = aes128_state[matrix_col][state_col] << 1u;
                    if (aes128_state[matrix_col][state_col] & 0x80)
                    {
                        mul_res ^= 0x1B;
                    }
                    if (aes128_column_mix_matrix[matrix_row][matrix_col] > 2u)
                    {
                        mul_res ^= aes128_state[matrix_col][state_col];
                    }
                }
                col_sum ^= mul_res;
            }
            state_column[matrix_row] = col_sum;
        }
        for (uint8_t state_row = 0; state_row < 4; state_row++)
        {
            aes128_state[state_row][state_col] = state_column[state_row];
        }
    }
}

void aes128_key_schedule (uint8_t round)
{
    uint8_t rcon[4u] = {aes128_rcon[round - 1u], 0u, 0u, 0u};
    uint8_t round_key_col[4u];

    uint8_t row = 0u, col = 3u, temp = aes128_round_key[row][col];
    while (row < 3u)
    {
        round_key_col[row] = aes128_round_key[row + 1u][col];
        row++;
    }
    round_key_col[row] = temp;

    for (row = 0u; row < 4u; row++)
    {
        round_key_col[row] = aes128_sBox[(((round_key_col[row]) & (0xF0u)) >> 4u)][((round_key_col[row]) & (0x0Fu))];
    }

    for (col = 0u; col < 4u; col++)
    {
        if (col == 0u)
        {
            for (row = 0u; row < 4u; row++)
            {
                aes128_round_key[row][col] ^= (rcon[row] ^ round_key_col[row]);
            }
        }
        else
        {
            for (row = 0u; row < 4u; row++)
            {
                aes128_round_key[row][col] ^= aes128_round_key[row][col - 1u];
            }
        }
    }
}

